package mx.edu.utng.hibernate06.orm;

import java.io.Serializable;

public class VentaDetalleId implements Serializable{
	private int idProd;
	private int idVen;
	public VentaDetalleId(int idProd, int idVen) {
		super();
		this.idProd = idProd;
		this.idVen = idVen;
	}
	public VentaDetalleId() {
		super();
	}
	public int getIdProd() {
		return idProd;
	}
	public void setIdProd(int idProd) {
		this.idProd = idProd;
	}
	public int getIdVen() {
		return idVen;
	}
	public void setIdVen(int idVen) {
		this.idVen = idVen;
	}
	

}
